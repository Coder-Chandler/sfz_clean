def province_and_city():
    province = {}
    for i in PROVINCE:
        index = i[0]
        name = i[1]
        for j in CITY:
            if j == index:
                province[name[:-1]] = CITY[j]
    for i in province:
        city = {}
        for j in province[i]:
            name = j[1][:-1]
            city[name] = name
        province[i] = city

    return province


